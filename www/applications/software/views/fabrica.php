	<div class="titulo-caja-texto">
		Proceso de desarrollo
	</div>
</div>
<hr class="clear">
<p>En Simpus seguimos la siguiente metodología para desarrollar los sistemas de información, en cada etapa mantenemos nuestra interacción con el cliente para que el sistema esté moldeado a su gusto y necesidad.</p>
<div style="float: left; width: 430px; padding: 20px">
	<div class="numero">1</div>
	<h3 style="margin-top:20px"><strong>Análisis de requerimientos: </strong></h3>
	<hr class="clear">
	<p class="justify">
	En esta etapa del proyecto se establecen las diferentes necesidades de la empresa o cliente, el cual realiza una descripción del problema. Esta primera etapa define el documento de especificación de requerimientos.	
	</p>
</div>	
<div style="float: right; width: 430px; padding: 20px">
	<div class="numero">2</div>
	<h3 style="margin-top:20px"><strong>Diseño del sistema: </strong></h3>
	<hr class="clear">
	<p class="justify">
		Se descompone y organiza el sistema en elementos que puedan elaborarse por separado, aprovechando las ventajas del desarrollo en equipo.  Se definen los componentes a utilizar en el desarrollo del producto de software. 
	</p>
</div>	
<div class="clear"></div>
<div style="float: left; width: 430px; padding: 20px">
	<div class="numero">3</div>
	<h3 style="margin-top:20px"><strong>Desarrollo: </strong></h3>
	<hr class="clear">
	<p class="justify">
	Se realiza la codificación del producto de software en base a los diagramas y documentos acordados en las fases de Análisis y Diseño del sistema, con la colaboración constante del cliente. 
	</p>
</div>	
<div style="float: right; width: 430px; padding: 20px">
	<div class="numero">4</div>
	<h3 style="margin-top:20px"><strong>Pruebas y validación: </strong></h3>
	<hr class="clear">
	<p class="justify">
	El cliente realiza una serie de pruebas de operación sobre el producto para determinar que el sistema es funcional en su totalidad.
	</p>
</div>	
<div class="clear"></div>
<div style="float: left; width: 430px; padding: 20px">
	<div class="numero">5</div>
	<h3 style="margin-top:20px"><strong>Implementación: </strong></h3>
	<hr class="clear">
	<p class="justify">
	En esta fase se encarga de capacitar a los usuarios en el manejo correcto del producto final, se entregan los manuales de usuario y técnicos para el correcto uso del sistema. El resultado final es el producto terminado en una versión RC del mismo. 
	</p>
</div>	
<div style="float: right; width: 430px; padding: 20px">
	<div class="numero">6</div>
	<h3 style="margin-top:20px"><strong>Mantenimiento: </strong></h3>
	<hr class="clear">
	<p class="justify">
	Para garantizar el correcto funcionamiento del sistema, nos mantenemos al pendiente.
	</p>
